#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int i;

	if (s[0] == c)
		return ((char *)s);
	i = 0;
	while (s[i++] != '\0')
	{
		if (s[i] == c)
			return ((char *)s + i);
	}
	return (NULL);
}
